# remove_node_modules.ps1
try {
  # Remove all items within node_modules
  Get-ChildItem -Path "node_modules" -Force | Remove-Item -Recurse -Force

  # Remove the node_modules directory itself
  Remove-Item -Path "node_modules" -Force
  Write-Host "Successfully removed node_modules"
} catch {
  Write-Host "Error removing node_modules: $($_.Exception.Message)"
}
